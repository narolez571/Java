package org.suggs.sandbox.jaxws.simple.client;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;

/**
 * Class to home to main method execution.
 * 
 * @author suggitpe
 * @version 1.0 10 Jun 2010
 */
public final class ServiceMain {

    private static final Log LOG = LogFactory.getLog( ServiceMain.class );

    public static void main( String[] aArgs ) {
        LOG.debug( "Calling webservice HelloService ..." );

        HelloWorldClient client = new HelloWorldClient();
        long start = System.currentTimeMillis();
        client.callWebService();
        long end = System.currentTimeMillis();

        LOG.debug( "Webservice call complete in [" + ( end - start ) + "] millis" );
    }

}
